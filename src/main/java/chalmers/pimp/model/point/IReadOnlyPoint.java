package chalmers.pimp.model.point;

/**
 * The {@code IReadOnlyPoint} represent a point that can be read.
 */
public interface IReadOnlyPoint {

  /**
   * Returns the x coordinate for the point.
   *
   * @return the x coordinate for the point.
   */
  int getX();

  /**
   * Returns the y coordinate for the point.
   *
   * @return the y coordinate for the point.
   */
  int getY();
}
